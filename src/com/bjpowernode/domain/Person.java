package com.bjpowernode.domain;

public class Person {
    private Integer id;
    private String name;
    private String address;
    private String gender;
    private Integer age;
    private String password;



    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
